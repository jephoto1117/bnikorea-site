# 파일 처리
# ============================================================
$htmlFiles = Get-ChildItem $siteRoot -Recurse -Filter "*.html" |
    Where-Object {
        $_.DirectoryName -ne $siteRoot -and
        $_.Name -notmatch "^naver" -and
        @("index.html","admin.html","setup.sql") -notcontains $_.Name
    } |
    Sort-Object FullName

$total = $htmlFiles.Count
$done  = 0
$utf8  = New-Object System.Text.UTF8Encoding $false

foreach ($file in $htmlFiles) {
    $done++
    if ($done % 200 -eq 0) { Write-Host "[$done/$total] 처리 중..." }

    $rel      = $file.FullName.Substring($siteRoot.Length + 1)
    $parts    = $rel.Split("\")
    $cat      = $parts[0]
    $pageNum  = [int]($file.BaseName) - 1   # 0-based index for arrays
    if ($pageNum -lt 0 -or $pageNum -gt 7) { $pageNum = 0 }

    $svc = if ($svcMap.ContainsKey($cat)) { $svcMap[$cat] } else { $cat }

    $c = [IO.File]::ReadAllText($file.FullName, [Text.Encoding]::UTF8)

    # ---- 1. 지역명 추출 (title에서 앞 2~3 토큰)
    $loc = if ($c -match '<title>([^|<]+?)(?:\s+\S+\s+\S+)?\s*(?:\|[^<]*)?\s*</title>') {
        # title 전체에서 서비스명 부분 제거하고 지역명만
        $t = if ($c -match '<title>([^<|]+)') { $matches[1].Trim() } else { "" }
        # 앞에서 2단어만 (예: "부산 해운대구" or "양산시 평산동")
        $words = $t -split '\s+'
        if ($words.Count -ge 2) { "$($words[0]) $($words[1])" } else { $t }
    } else { $cat }

    # ---- 2. title 에서 "| 즐거운탱이네" 제거
    $c = $c -replace '\s*\|\s*즐거운탱이네', ''

    # ---- 3. JSON-LD name 교체
    $c = $c -replace '"name":\s*"마케팅 플레이스 블로그 홍보 문의"', "`"name`": `"$loc $svc 업체 안내`""

    # ---- 4. intro-box 단락 교체 (페이지번호별 8종)
    $intro = $introTpl[$pageNum] -replace '\{L\}', $loc -replace '\{S\}', $svc
    $c = [regex]::Replace($c,
        '(<p class="intro-box"[^>]*>)[^<]*(</p>)',
        "`$1$intro`$2")

    # ---- 5. FAQ 전체 교체
    #   제거 대상: 고아 </summary> + faq-a div + </details>  및  이어지는 <details class="faq">...</details> 들
    #   교체 대상: <!-- 페이지네이션 --> 직전의 FAQ 덩어리 전체

    if ($faqMap.ContainsKey($cat)) {
        $faqs = $faqMap[$cat]

        # 새 FAQ HTML 생성
        $newFaqHtml = "`n  <!-- FAQ -->`n  <div class=`"faq-list`">`n"
        foreach ($pair in $faqs) {
            $q = $pair[0] -replace '\{L\}', $loc -replace '\{S\}', $svc
            $a = $pair[1] -replace '\{L\}', $loc -replace '\{S\}', $svc
            $newFaqHtml += "    <details class=`"faq`">`n"
            $newFaqHtml += "      <summary class=`"faq-q`">$q</summary>`n"
            $newFaqHtml += "      <div class=`"faq-a`">$a</div>`n"
            $newFaqHtml += "    </details>`n"
        }
        $newFaqHtml += "  </div>`n`n  "

        # 기존 FAQ 블록 제거 + 교체
        # 패턴: (선택적 고아 </summary>...<div class="faq-a">...</div></details>) + 이어지는 <details> 블록들 + pagination 직전
        $c = [regex]::Replace($c,
            '(?s)(\s*</summary>\s*<div class="faq-a">.*?</details>\s*)?(\s*<details class="faq">.*?</details>\s*)+(\s*</div>\s*\n+\s*<!-- 페이지네이션 -->)',
            "`n  </div>`n`n  $newFaqHtml<!-- 페이지네이션 -->")

        # 위 패턴이 맞지 않으면 단순 교체 시도 (details 블록만 있는 경우)
        if ($c -notmatch "faq-list") {
            # faq-list div 없이 details 만 있을 때
            $c = [regex]::Replace($c,
                '(?s)(<div class="faq-list">)(.*?)(</div>\s*\n+\s*<!-- 페이지네이션 -->)',
                "<div class=`"faq-list`">`n$($newFaqHtml -replace '  <div class=`"faq-list`">','' -replace '  </div>','')</div>`n`n  <!-- 페이지네이션 -->")
        }
    }

    # ---- 6. 파일 저장
    [IO.File]::WriteAllText($file.FullName, $c, $utf8)
}

Write-Host "`n완료: $total 개 파일 처리됨" -ForegroundColor Green
